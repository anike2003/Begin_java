//program to compute the average of two numbers
public class average_of_two_num
     {
           public static void main(String[] args)
           {   //declaring variables
               double num1=5.0;
               double num2=7.0;
               double avg;
               //initializing average
               avg = (num1 + num2)/2;
               //To print output
               System.out.print("The average of the two numbers is " + avg);
           }
      }
    