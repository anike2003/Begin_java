//Program of calculating the area of a circle
public class Area_circle
{
  public static void main(String[] args)
  { //declaring variables
      double radius = 3;
      double pie = 3.142;
      double Area = pie * radius * radius;
      //print result
      System.out.print("The area of the cicle with radius " + radius + " is " + Area);


  }
}